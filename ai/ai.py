from openai import OpenAI
from typing import Dict, Optional
import logging

logger = logging.getLogger(__name__)


class AnketAnalyzer:
    def __init__(self, openai_api_key: str):
        self.client = OpenAI(api_key=openai_api_key, base_url="https://api.aitunnel.ru/v1/")

    async def analyze_peer_anketas(self, peer_id: int, anketa_data: list) -> str:
        """Анализ анкеты с обработкой ошибок"""
        try:
            response = await self._get_ai_analysis(self._build_prompt({
                "Раздел 1. Профессиональное самоопределение": anketa_data
            }))

            if not response or not response.strip():
                logger.warning(f"OpenAI вернул пустой ответ для peer_id={peer_id}")
                return "🤖 Не удалось получить анализ. Пожалуйста, попробуйте позже."

            return response.strip()

        except Exception as e:
            logger.error(f"Ошибка анализа для peer_id={peer_id}: {e}")
            return f"❌ Ошибка анализа. Попробуйте позже."

    def _build_prompt(self, anketa_data: Dict) -> str:
        """Сборка промпта с правилами проверки"""
        sections = []
        filled = []

        for name, data in anketa_data.items():
            status = "ЗАПОЛНЕН" if data else "НЕ ЗАПОЛНЕН"
            if data:
                filled.append(name)
                answers = "\n".join([f"❓ {q}\nОтвет: {a}\n" for q, a in data.items()])
                sections.append(f"📂 {name} ({status})\n{'=' * 50}\n{answers}")
            else:
                sections.append(f"📂 {name} ({status})\n{'=' * 50}\n❌ Пользователь не отвечал\n")

        prompt = f"""
{chr(10).join(sections)}

📊 Статус заполнения: {', '.join(filled) if filled else 'Нет заполненных разделов'}


⚠️ ВАЖНЫЕ ПРАВИЛА:
1. Если раздел НЕ ЗАПОЛНЕН (помечен как "НЕ ЗАПОЛНЕН") - автоматически ставится 0 баллов за весь раздел
2. Для заполненных разделов считай баллы строго по разбаловке в квадратных скобках
3. НЕ ПРИДУМЫВАЙ ответы за пользователя - если раздела нет, значит баллов нет

ЗАДАЧА:
1. Посчитай баллы ТОЛЬКО для ЗАПОЛНЕННЫХ разделов строго по разбаловке в квадратных скобках
2. Для НЕЗАПОЛНЕННЫХ разделов ставь 0 баллов
3. Для каждого раздела дай интерпретацию, используя ТОЛЬКО следующие тексты (выбирай строго по диапазону баллов):

Профессиональное самоопределение:
• 15-17 балла: Ты очень уверенный, понимающий свой путь к реализации человек. Скорее всего ты уже знаешь себя и мир профессий достаточно, чтобы сделать осознанный выбор. Поэтому я могу только пожелать тебе реализовать все свои таланты.
• 10-14 балла: Ты достаточно хорошо понимаешь свои сильные стороны и уже выбрал направление развития. Советую тебе обсудить свой выбор с вдохновителями, которые уже реализовались в выбранной тобой сфере. Расспроси их о том, какие шаги лучше сделать в первую очередь и получи контакты полезных людей.
• 5-9 баллов: У тебя есть сомнения. И это нормально. Даже хорошо. Потому что это значит, что ты думаешь и ищешь лучшее приложение для своих талантов. Наш тренинг «Путь к самореализации» идеально подойдет тебе для того, чтобы лучше понять свои стремления, увидеть свою силу и выявить лучший путь применения твоих талантов.
• 0-4 баллов: Дружище, кажется ты в тупике. Возможно тебе неинтересно узнать себя лучше или же что-то тебе мешает. Хотим тебе помочь. Обязательно подойди к тренерам и вдохновителям и поговори по душам. А еще я очень советую тебе отправиться на тренинг «Путь к самореализации». Он поможет тебе исследовать свои интересы и способности, определить пути развития и реализации твоего потенциала, разобраться в том, что стоит у тебя на пути.

⚠️ КРИТИЧЕСКИ ВАЖНО: В ответе выводи ТОЛЬКО текст интерпретации, БЕЗ указания баллов и диапазонов. Например, если пользователь набрал 4 балла в Разделе "Профессиональное самоопределение", нужно вывести только: "Дружище, кажется ты в тупике. Возможно тебе неинтересно узнать себя лучше или же что-то тебе мешает. Хотим тебе помочь. Обязательно подойди к тренерам и вдохновителям и поговори по душам. А еще я очень советую тебе отправиться на тренинг «Путь к самореализации». Он поможет тебе исследовать свои интересы и способности, определить пути развития и реализации твоего потенциала, разобраться в том, что стоит у тебя на пути.

Формат ответа (строго соблюдай!):
[текст интерпретации для набранных баллов без ковычек в начале и конце]
"""
        return prompt

    async def _get_ai_analysis(self, prompt: str) -> Optional[str]:
        """Запрос к OpenAI"""
        try:
            response = self.client.chat.completions.create(
                model="gpt-5-mini-2025-08-07",
                messages=[
                    {
                        "role": "system",
                        "content": """Ты профориентолог. КРИТИЧЕСКИ ВАЖНО:
1. Если раздел помечен как "НЕ ЗАПОЛНЕН" - ставь 0 баллов
2. НИКОГДА не придумывай ответы за пользователя
3. Для заполненных разделов используй разбаловку в [скобках]
4. ФОРМАТ ОТВЕТА: ТОЛЬКО текст интерпретации, без цифр и диапазонов"""
                    },
                    {"role": "user", "content": prompt}
                ],
                max_tokens=2000,  #
                temperature=0.2
            )

            content = response.choices[0].message.content
            return content if content and content.strip() else None

        except Exception as e:
            logger.error(f"OpenAI API error: {e}")
            return None