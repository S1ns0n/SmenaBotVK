from openai import OpenAI
from typing import Dict, Optional
from database import db_manager


class AnketAnalyzer:
    def __init__(self, openai_api_key: str):
        self.client = OpenAI(api_key=openai_api_key, base_url="https://api.aitunnel.ru/v1/")

    async def analyze_peer_anketas(self, peer_id: int) -> Optional[str]:
        try:
            anketa0_data = await db_manager.get_anketa_data(peer_id, "anketa0")
            anketa1_data = await db_manager.get_anketa_data(peer_id, "anketa1")
            anketa2_data = await db_manager.get_anketa_data(peer_id, "anketa2")
            anketa3_data = await db_manager.get_anketa_data(peer_id, "anketa3")

            if not any([anketa0_data, anketa1_data, anketa2_data, anketa3_data]):
                return "❌ Заполните хотя бы одну анкету для анализа."

            prompt = self._build_prompt({
                "Раздел 1. Персональные данные": anketa0_data,
                "Раздел 2. Профессиональное самоопределение": anketa1_data,
                "Раздел 3. Мечта и путь к ней": anketa2_data,
                "Раздел 4. Модель героя-созидателя": anketa3_data
            })

            response = await self._get_ai_analysis(prompt)
            return response

        except Exception as e:
            return f"❌ Ошибка анализа: {str(e)}"

    def _build_prompt(self, anketas_data: Dict) -> str:
        prompt_parts = ["📋 АНКЕТА ПРОФОРИЕНТАЦИИ\n\n"]

        # Отмечаем, какие разделы заполнены
        filled_sections = []
        for section_name, data in anketas_data.items():
            if data:
                filled_sections.append(section_name)
                prompt_parts.append(f"📂 {section_name} (ЗАПОЛНЕН)")
                prompt_parts.append("=" * 50)

                for question, answer in data.items():
                    prompt_parts.append(f"❓ {question}")
                    prompt_parts.append(f"Ответ: {answer}")
                    prompt_parts.append("")

                prompt_parts.append("")
            else:
                prompt_parts.append(f"📂 {section_name} (НЕ ЗАПОЛНЕН)")
                prompt_parts.append("=" * 50)
                prompt_parts.append("❌ Пользователь не отвечал на вопросы этого раздела\n\n")

        full_data = "\n".join(prompt_parts)

        # Создаем инструкцию в зависимости от заполненных разделов
        sections_status = "\n".join([f"- {section}" for section in filled_sections]) if filled_sections else "Нет заполненных разделов"

        analysis_prompt = f"""{full_data}

📊 СТАТУС ЗАПОЛНЕНИЯ:
Заполненные разделы: {sections_status}

⚠️ ВАЖНЫЕ ПРАВИЛА:
1. Если раздел НЕ ЗАПОЛНЕН (помечен как "НЕ ЗАПОЛНЕН") - автоматически ставится 0 баллов за весь раздел
2. Для заполненных разделов считай баллы строго по разбаловке в квадратных скобках
3. НЕ ПРИДУМЫВАЙ ответы за пользователя - если раздела нет, значит баллов нет

ЗАДАЧА:
1. Посчитай баллы ТОЛЬКО для ЗАПОЛНЕННЫХ разделов строго по разбаловке в квадратных скобках
2. Для НЕЗАПОЛНЕННЫХ разделов ставь 0 баллов
3. Для каждого раздела дай интерпретацию, используя ТОЛЬКО следующие тексты (выбирай строго по диапазону баллов):

Раздел 2 (Профессиональное самоопределение):
• 25-32 балла: Ты очень уверенный, понимающий свой путь к реализации человек. Скорее всего ты уже знаешь себя и мир профессий достаточно, чтобы сделать осознанный выбор. Поэтому я могу только пожелать тебе реализовать все свои таланты.
• 20-24 балла: Ты достаточно хорошо понимаешь свои сильные стороны и уже выбрал направление развития. Советую тебе обсудить свой выбор с вдохновителями, которые уже реализовались в выбранной тобой сфере. Расспроси их о том, какие шаги лучше сделать в первую очередь и получи контакты полезных людей.
• 10-19 баллов: У тебя есть сомнения. И это нормально. Даже хорошо. Потому что это значит, что ты думаешь и ищешь лучшее приложение для своих талантов. Наш тренинг «Путь к самореализации» идеально подойдет тебе для того, чтобы лучше понять свои стремления, увидеть свою силу и выявить лучший путь применения твоих талантов.
• 0-9 баллов: Дружище, кажется ты в тупике. Возможно тебе неинтересно узнать себя лучше или же что-то тебе мешает. Хотим тебе помочь. Обязательно подойди к тренерам и вдохновителям и поговори по душам. А еще я очень советую тебе отправиться на тренинг «Путь к самореализации». Он поможет тебе исследовать свои интересы и способности, определить пути развития и реализации твоего потенциала, разобраться в том, что стоит у тебя на пути.

Раздел 3 (Мечта и путь к ней):
• 17-23 балла: Ты хорошо понимаешь свои желания, у тебя есть стратегия, которая позволяет достигать их. Это хороший знак. Путь у тебя все получится!
• 9-16 баллов: Ты пока в поиске ответов. Это прекрасно! Значит ты думающий человек! Наш тренинг «Формула мечты» - это идеальная площадка для твоей глубокой работы с самим собой. Если ты любишь смотреть глубоко, если тебе интересно, откуда появляются твои желания и как научиться формулировать их и исполнять их так, чтобы это делало тебя счастливым, то записывайся на этот тренинг. Ты не только получишь полезные навыки, но и выстроишь четкую картину мира.
• 0-8 баллов: Ты пока не чувствуешь себя уверенно в этой теме. Сам факт наличия мечты и формирования желаний для тебя еще не пройденный этап, а путь их реализации - вообще тебе неизвестен. Значит наш тренинг «Формула мечты» точно будет тебе полезен. Ты узнаешь все, что нужно знать о мечтах, о желаниях и о счастье, научишься слушать себя, определять свои желания так, чтобы они тебя вдохновляли и чтобы действительно приносили тебе счастье, научишься ставить цели и точно спланируешь все шаги, которые помогут тебе успешно реализовать задуманное.

Раздел 4 (Модель героя-созидателя):
• 27-33 балла: Ты двигаешься вперед точь-в-точь по модели героя-созидателя. Это значит, что ты активно развиваешься и растешь, опираясь на все элементы модели. Более того, ты понимаешь значимость и смысл каждого элемента модели и это помогает тебе уверенно идти вперед. Это круто!
• 20-26 баллов: Продолжай! Не сбавляй обороты! Ты идешь в верном направлении. Советую тебе больше времени провести с вдохновителями, чтобы насытиться их опытом и состоянием. И выбирай тренинг по вкусу, чтобы с радостью и с пользой для себя провести время.
• 10-19 баллов: Дорогой друг! Ты пока в самом начале своего пути самоисследования. Тебе будет полезно хорошенько разобраться в том, что делает человека успешным и самодостаточным, а затем пройти разнообразные практики саморазвития. Запишись на тренинг по модели героя-созидателя, и выйди на новый уровень самопонимания!
• 0-9 баллов: Вероятно, тебе не очень интересно исследовать самого себя. А ведь именно это помогает двигаться к мечтам, реализовывать желания и достигать целей. Тренинг по модели героя-созидателя поможет тебе лучше разобраться в том, какие значимые элементы позволяют личности достичь высот и прокачать свои навыки и личностные качества для того, чтобы твоя личность обрела внутренние опоры.

⚠️ КРИТИЧЕСКИ ВАЖНО: В ответе выводи ТОЛЬКО текст интерпретации в кавычках, БЕЗ указания баллов и диапазонов. Например, если пользователь набрал 22 балла в Разделе 4, нужно вывести только: "Продолжай! Не сбавляй обороты! Ты идешь в верном направлении. Советую тебе больше времени провести с вдохновителями, чтобы насытиться их опытом и состоянием. И выбирай тренинг по вкусу, чтобы с радостью и с пользой для себя провести время.

Формат ответа (строго соблюдай!):
Раздел 2 (Профессиональное самоопределение):
[текст интерпретации для набранных баллов без ковычек в начале и конце]

Раздел 3 (Мечта и путь к ней):
[текст интерпретации для набранных баллов без ковычек в начале и конце]

Раздел 4 (Модель героя-созидателя):
[текст интерпретации для набранных баллов без ковычек в начале и конце]"""
        return analysis_prompt

    async def _get_ai_analysis(self, prompt: str) -> str:
        response = self.client.chat.completions.create(
            model="gpt-4o-mini",
            messages=[
                {
                    "role": "system",
                    "content": """Ты профориентолог. КРИТИЧЕСКИ ВАЖНЫЕ ПРАВИЛА:

1. Если раздел помечен как "НЕ ЗАПОЛНЕН" - значит пользователь не отвечал на вопросы этого раздела. АВТОМАТИЧЕСКИ СТАВЬ 0 БАЛЛОВ за весь раздел.

2. НИ В КОЕМ СЛУЧАЕ не придумывай ответы и не додумывай баллы за пользователя. Если раздела нет - баллов нет.

3. Для заполненных разделов:
   - В каждом вопросе есть варианты ответов с баллами в квадратных скобках
   - Ответы пользователя могут быть в виде цифр (1,2,3...), букв (А,Б,В) или текста
   - Цифра "1" означает первый вариант ответа
   - Последовательность цифр (4321) означает порядок расстановки пунктов

4. Для заданий на расстановку:
   - 3 балла, если последовательность изменена
   - 0 баллов, если последовательность идет по порядку или ответа нет

5. Будь строг в подсчетах. Если ответ не соответствует ни одному варианту - ставь 0 баллов за вопрос.

6. ФОРМАТ ОТВЕТА - СТРОГОЕ ПРАВИЛО:
В ответе нужно вывести ТОЛЬКО текст интерпретации, БЕЗ каких-либо цифр, диапазонов или пометок.

НЕПРАВИЛЬНО (так нельзя): "0-9 баллов: Дружище, кажется ты в тупике..."
ПРАВИЛЬНО: "Дружище, кажется ты в тупике..."

НЕПРАВИЛЬНО: "• 27-33 балла: Ты двигаешься вперед..."
ПРАВИЛЬНО: "Ты двигаешься вперед..."

Помни: ЛУЧШЕ ПОСТАВИТЬ 0 БАЛЛОВ, ЧЕМ ПРИДУМАТЬ ИХ ЗА ПОЛЬЗОВАТЕЛЯ!"""
                },
                {"role": "user", "content": prompt}
            ],
            max_tokens=1500,
            temperature=0.2
        )

        return response.choices[0].message.content